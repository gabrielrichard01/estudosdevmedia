var logico = true;
var num = 10;
var texto = "10";

// OPERADOR DE IGUALDADE
console.log(logico = num == texto);
console.log(logico = num === texto);

// OPERADOR DE DESIGUALDADE
console.log(logico = num != texto);
console.log(logico = num !== texto);

// OPERADOR MAIOR OU IGUAL A
console.log(logico = num > texto);
console.log(logico = num >= texto);

// OPERADOR MENOR OU IGUAL A
console.log(logico = num < texto);
console.log(logico = num <= texto);